package classwork.calc;

public class Calculator {

    public int addition(int a, int b) {
        return a + b;
    }

    public int subtraction(int a, int b) {
        return a - b;
    }

    public long multiplication(int a, int b) {
        return (long) a * b;
    }

    public double division(int a, int b) {
        return (double) a / b;
    }
}
