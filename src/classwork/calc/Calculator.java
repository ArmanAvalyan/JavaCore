package classwork.calc;

public class Calculator {

    int addition(int a, int b) {
        return a + b;
    }

    int subtraction(int a, int b) {
        return a - b;
    }

    long multiplication(int a, int b) {
        return (long) a * b;
    }

    double division(int a, int b) {
        return (double) a / b;
    }
}
